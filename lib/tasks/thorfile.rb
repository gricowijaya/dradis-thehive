class TheHiveTasks < Thor
  include Rails.application.config.dradis.thor_helper_module

  namespace "dradis:plugins:thehive"

  desc      "upload FILE", "upload TheHive results in JSON format"
  long_desc "This plugin expect a JSON file generated by TheHive using: -f "\
            "jason -o results.json"
  def upload(file_path)
    require 'config/environment'

    unless File.exists?(file_path)
      $stderr.puts "** the file [#{file_path}] does not exist"
      exit(-1)
    end

    detect_and_set_project_scope

    importer = Dradis::Plugins::TheHive::Importer.new(task_options)
    importer.import(file: file_path)
  end

end
